package arrays;// Assignment 38- write a program to store 3 fruit names in a 1-D array

public class Fruit {
	
	public static void main(String[] args) {
		
		String [] fruit = new String [3];
		
		fruit [0] = "Apple";
		fruit [1] = "Mango";
		fruit [2] = "Grapes";
		
		System.out.println(fruit [0]);
		System.out.println(fruit [1]);
		System.out.println(fruit [2]);
	}

}
